package main

import (
	"image/color"
	"time"

	"github.com/mohanson/aiball-creator"
)

func main() {
	aibo := aiball.NewAIBall(18, 32)
	aibo.RGBABase = color.RGBA{0xff, 0xff, 0xff, 0xff}
	aibo.RGBA = []color.RGBA{
		{0xeb, 0xed, 0xf0, 0xff},
		{0xd1, 0xba, 0x74, 0xff},
		{0xf4, 0x60, 0x6c, 0xff},
		{0x19, 0xca, 0xad, 0xff},
		{0x9c, 0x9c, 0x9c, 0xff},
		{0x2f, 0x4f, 0x4f, 0xff},
		{0xf4, 0x60, 0x6c, 0xff},
	}
	aibo.CellSize = 26
	aibo.CellPads = 4

	aibo.Cell[4][2] = 2
	aibo.Join(time.Millisecond * 100)
	aibo.Save("/tmp/foo.gif")
}
